class Symbol {

	constructor(n) {
		this.n = n;
		this.el = document.createElement('div');
		this.el.classList.add('cell');
		this.el.innerHTML = n;
	}

	hide() {
		this.el.classList.remove('active');
	}

	show() {
		this.el.classList.add('active');
	}

}

